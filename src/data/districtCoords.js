/**
 * 지역명 문자열에서 좌표를 찾습니다.
 * "서울특별시 송파구" → 송파구 좌표
 * "대구광역시 달성군" → 달성군 좌표
 */
export function lookupRegionCoords(region) {
  if (!region) return null

  // 1. 정확 매칭
  if (COORDS[region]) return COORDS[region]

  // 2. 긴 키부터 부분 매칭 (더 구체적인 것 우선)
  const sorted = Object.keys(COORDS).sort((a, b) => b.length - a.length)
  for (const key of sorted) {
    if (region.includes(key)) return COORDS[key]
  }

  return null
}
